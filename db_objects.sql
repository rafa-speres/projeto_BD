-- Procedure para gerar relat�rio de um L�der de Fac��o

-- VERS�O FUNCIONAL COM AGRUPAMENTO POR ESP�CIE OU PLANETA (+ CASO DEFAULT)

CREATE OR REPLACE PROCEDURE relatorio_lider_faccao(p_oficial_id IN CHAR, p_agrupamento IN VARCHAR2 DEFAULT NULL)
IS
    -- Vari�veis para armazenar informa��es intermedi�rias
    v_faccao_nome VARCHAR2(15);
BEGIN
    -- Buscar o nome da fac��o do l�der com o CPI informado
    SELECT FACCAO.NOME
    INTO v_faccao_nome
    FROM FACCAO
    JOIN LIDER ON FACCAO.LIDER = LIDER.CPI
    WHERE LIDER.CPI = p_oficial_id;

    -- Verificar se a fac��o foi encontrada
    IF v_faccao_nome IS NOT NULL THEN

        -- Caso DEFAULT: informa��es sobre as comunidades da fac��o do l�der
        IF p_agrupamento IS NULL THEN
            FOR rec IN (
                SELECT C.ESPECIE, C.NOME AS COMUNIDADE, C.QTD_HABITANTES
                FROM COMUNIDADE C
                JOIN PARTICIPA P ON C.ESPECIE = P.ESPECIE AND C.NOME = P.COMUNIDADE
                WHERE P.FACCAO = v_faccao_nome
            ) LOOP
                DBMS_OUTPUT.PUT_LINE('Fac��o: ' || v_faccao_nome);
                DBMS_OUTPUT.PUT_LINE('Comunidade: ' || rec.COMUNIDADE);
                DBMS_OUTPUT.PUT_LINE('Esp�cie: ' || rec.ESPECIE);
                DBMS_OUTPUT.PUT_LINE('Total de Habitantes: ' || rec.QTD_HABITANTES);
                DBMS_OUTPUT.PUT_LINE('-------------------------------');
            END LOOP;

        -- Agrupamento por esp�cie
        ELSIF p_agrupamento = 'ESPECIE' THEN
            FOR rec IN (
                SELECT C.ESPECIE, COUNT(C.NOME) AS QTD_COMUNIDADES, SUM(C.QTD_HABITANTES) AS TOTAL_HABITANTES -- Aqui somamos a quantidade de habitantes agrupadas por ESPECIE, para as COMUNIDADES participantes da FACCAO do LIDER em quest�o.
                FROM COMUNIDADE C
                JOIN PARTICIPA P ON C.ESPECIE = P.ESPECIE AND C.NOME = P.COMUNIDADE
                WHERE P.FACCAO = v_faccao_nome
                GROUP BY C.ESPECIE
            ) LOOP
                DBMS_OUTPUT.PUT_LINE('Fac��o: ' || v_faccao_nome);
                DBMS_OUTPUT.PUT_LINE('Comunidades: ' || rec.QTD_COMUNIDADES);
                DBMS_OUTPUT.PUT_LINE('Esp�cie: ' || rec.ESPECIE);
                DBMS_OUTPUT.PUT_LINE('Total de Habitantes: ' || rec.TOTAL_HABITANTES);
                DBMS_OUTPUT.PUT_LINE('-------------------------------');
            END LOOP;

        -- Agrupamento por planeta
        ELSIF p_agrupamento = 'PLANETA' THEN
            FOR rec IN (
                SELECT H.PLANETA, COUNT(DISTINCT C.NOME) AS QTD_COMUNIDADES, SUM(C.QTD_HABITANTES) AS TOTAL_HABITANTES -- Aqui somamos a quantidade de habitantes das COMUNIDADES participantes da FACCAO do LIDER em quest�o.
                FROM COMUNIDADE C
                JOIN PARTICIPA P ON C.ESPECIE = P.ESPECIE AND C.NOME = P.COMUNIDADE
                JOIN HABITACAO H ON C.ESPECIE = H.ESPECIE AND C.NOME = H.COMUNIDADE
                WHERE P.FACCAO = v_faccao_nome
                GROUP BY H.PLANETA
            ) LOOP
                DBMS_OUTPUT.PUT_LINE('Fac��o: ' || v_faccao_nome);
                DBMS_OUTPUT.PUT_LINE('Comunidades: ' || rec.QTD_COMUNIDADES);
                DBMS_OUTPUT.PUT_LINE('Planeta: ' || rec.PLANETA);
                DBMS_OUTPUT.PUT_LINE('Total de Habitantes: ' || rec.TOTAL_HABITANTES);
                DBMS_OUTPUT.PUT_LINE('-------------------------------');
            END LOOP;

        ELSE
            DBMS_OUTPUT.PUT_LINE('Agrupamento inv�lido. Use "ESPECIE" ou "PLANETA" como par�metro.');
        END IF;

    ELSE
        DBMS_OUTPUT.PUT_LINE('Nenhuma fac��o encontrada para o l�der com CPI: ' || p_oficial_id);
    END IF;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('Nenhum l�der encontrado com o CPI: ' || p_oficial_id);
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('Erro: ' || SQLERRM);
END;


