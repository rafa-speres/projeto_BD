    p_nome IN VARCHAR2,
    p_classificacao IN VARCHAR2,
    p_massa IN NUMBER,
    p_x IN NUMBER,
    p_y IN NUMBER,
    p_z IN NUMBER
  ) IS
  BEGIN
    BEGIN
      INSERT INTO ESTRELA (ID_ESTRELA, NOME, CLASSIFICACAO, MASSA, X, Y, Z)
