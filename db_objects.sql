  END deletar_estrela;

  -- Relativo ao relatório -------------------------------------------------------------

  FUNCTION obterEstrelas RETURN tp_tabela_estrela PIPELINED AS
  BEGIN
    FOR rec IN (
      SELECT ID_ESTRELA, NOME, CLASSIFICACAO, MASSA, X, Y, Z
      FROM ESTRELA
      WHERE MASSA IS NULL
    ) LOOP
      PIPE ROW(tp_estrela(
        rec.ID_ESTRELA,
        rec.NOME,
        rec.CLASSIFICACAO,
        rec.MASSA,
        rec.X,
        rec.Y,
        rec.Z
      ));
    END LOOP;
  EXCEPTION
    WHEN OTHERS THEN
      DBMS_OUTPUT.PUT_LINE('Erro ao obter estrelas: ' || SQLERRM);
      RAISE;
  END obterEstrelas;
  
  
  FUNCTION obterPlanetas RETURN tp_tabela_planeta PIPELINED AS
  BEGIN
    FOR rec IN (
      SELECT ID_ASTRO, RAIO, CLASSIFICACAO, MASSA
      FROM Planeta
      WHERE MASSA IS NULL
    ) LOOP
      PIPE ROW(tp_planeta(
        rec.ID_ASTRO,
        rec.RAIO,
        rec.CLASSIFICACAO,
        rec.MASSA
      ));
    END LOOP;
  EXCEPTION
    WHEN OTHERS THEN
      DBMS_OUTPUT.PUT_LINE('Erro ao obter planetas: ' || SQLERRM);
      RAISE;
  END obterPlanetas;
  
  
  FUNCTION obterSistemas RETURN tp_tabela_sistema PIPELINED AS
  BEGIN
    FOR rec IN (
      SELECT ESTRELA, NOME
      FROM SISTEMA
      WHERE NOME IS NULL
    ) LOOP
      PIPE ROW(tp_sistema(
        rec.ESTRELA,
        rec.NOME
      ));
    END LOOP;
  EXCEPTION
    WHEN OTHERS THEN
      DBMS_OUTPUT.PUT_LINE('Erro ao obter sistemas: ' || SQLERRM);
      RAISE;
  END obterSistemas;

END PACOTE_CIENTISTA;




