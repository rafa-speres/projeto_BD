    p_id_estrela IN VARCHAR2
  ) IS
  BEGIN
    BEGIN
      DELETE FROM ESTRELA
      WHERE ID_ESTRELA = p_id_estrela;
      
      IF SQL%ROWCOUNT = 0 THEN
        DBMS_OUTPUT.PUT_LINE('Erro: Nenhuma estrela encontrada com o ID fornecido.');
      END IF;
    EXCEPTION
      WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('Erro: ' || SQLERRM);
    END;
  END deletar_estrela;

